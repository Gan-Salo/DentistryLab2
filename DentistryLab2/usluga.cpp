#include "usluga.h"

struct usluga usl_create()
{
	usluga data;
	data.title = "Тестовая услуга";
	data.cost = 700;
	return data;
}

struct usluga usl_create(string ti, int co)
{
	usluga data;
	data.title = ti;
	data.cost = co;
	return data;
}

void usl_view(usluga a)
{
	cout << "_Кабинет_\n";
	cout << "Название услуги: ";
	cout << a.title;
	cout << " || Стоимость услуги: ";
	cout << a.cost;
	cout << " ||\n\n";
}